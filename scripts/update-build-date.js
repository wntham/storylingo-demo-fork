#!/usr/bin/env node
/**
 * Generates client/constants/buildDate.ts with the current build timestamp.
 * Run via: node scripts/update-build-date.js
 * Hooked automatically into npm predev and prebuild scripts.
 */

const fs = require("fs");
const path = require("path");

const now = new Date();

// Format: "Mar 4, 2026 @ 09:05"
const formatted = now.toLocaleString("en-US", {
  month: "short",
  day: "numeric",
  year: "numeric",
  hour: "2-digit",
  minute: "2-digit",
  hour12: false,
  timeZone: Intl.DateTimeFormat().resolvedOptions().timeZone,
}).replace(",", "").replace(" at ", " @ ").replace(/(\d{4}),? /, "$1 @ ");

// Simpler, reliable formatting
const months = ["Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"];
const month = months[now.getMonth()];
const day = now.getDate();
const year = now.getFullYear();
const hours = String(now.getHours()).padStart(2, "0");
const minutes = String(now.getMinutes()).padStart(2, "0");
const buildDate = `${month} ${day}, ${year} @ ${hours}:${minutes}`;

const outPath = path.join(__dirname, "../client/constants/buildDate.ts");
const content = `// Auto-generated by scripts/update-build-date.js — do not edit manually.
export const BUILD_DATE = "${buildDate}";
`;

fs.writeFileSync(outPath, content, "utf-8");
console.log(`✅ Build date updated: ${buildDate}`);
